#ifndef TYPES_H
#define TYPES_H
#include<stdint.h>



// rust types
typedef int8_t i8;
typedef int16_t i16;
typedef int32_t i32;
typedef int64_t i64;

typedef uint8_t u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;
// pointer width
typedef intptr_t isize;
typedef uintptr_t usize;

// 128 width integers
typedef __int128 i128;
typedef unsigned __int128 u128;

#endif // TYPES_H
